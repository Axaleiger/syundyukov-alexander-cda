seed_state = 42


def _seeded_random() -> float:
    global seed_state
    seed_state = (seed_state * 9301 + 49297) % 233280
    return seed_state / 233280


def _seeded_int(a: int, b: int) -> int:
    return int(_seeded_random() * (b - a + 1)) + a


def _seeded_choice(arr: list) -> str:
    return arr[_seeded_int(0, len(arr) - 1)]


def _random_date_in_range() -> str:
    start = datetime(2025, 11, 1).date()
    end = datetime(2026, 3, 1).date()
    delta = (end - start).days
    d = start + timedelta(days=int(_seeded_random() * delta))
    return f"{d.day:02d}.{d.month:02d}.{d.year}"


def _random_time() -> str:
    h = _seeded_int(1, 3)
    m = _seeded_int(0, 59)
    return f"{h} ч {m} мин"


def generate_scenarios() -> list[dict]:
    global seed_state
    seed_state = 42
    out: list[dict] = []
    for i, (name, stage_type) in enumerate(SCENARIO_NAMES_WITH_STAGE):
        field = _seeded_choice(FIELDS)
        out.append(
            {
                "id": f"SC-{17000 + i * 111 + _seeded_int(0, 99)}",
                "name": name,
                "stages": _seeded_int(5, 10),
                "do": FIELD_TO_DO[field],
                "field": field,
                "status": _seeded_choice(STATUSES),
                "approved": _seeded_random() > 0.4,
                "dateCreated": _random_date_in_range(),
                "timeCalc": _random_time(),
                "dateUpdated": _random_date_in_range(),
                "author": _seeded_choice(AUTHORS),
                "stageType": stage_type,
                "direction": _seeded_choice(SCENARIO_DIRECTIONS),
            }
        )
