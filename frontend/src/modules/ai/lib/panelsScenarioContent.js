/** Сжатые шаги для панели «Мышление» (по одному на карточку + таблица). */
export function getThinkingStepsFromPanels(preset) {
	const block = PANELS_SCENARIO_CONTENT[preset]
	if (!block) return []
	const out = []
	for (const c of block.cards) {
		out.push(`Сценарий (panels): ${c.title}`)
	}
	out.push(`Показатели панели: ${block.tableLines.join(" · ")}`)
	return out
}
