/** Нормализованный ключ: коды через «|» в порядке localeCompare('ru'). */
function sortedKey(codes) {
	return [...codes].sort((a, b) => a.localeCompare(b, 'ru')).join('|')
}

/**
 * Блок про сквозные процессы для комбинации кодов на одном ребре причина↔гипотеза.
 * Только совпадения из перечня заказчика; при отсутствии совпадения — пустая строка.
 */
export function formatChEdgeBusinessLines(unionCodes) {
	const key = sortedKey(unionCodes)
	const header = 'Бизнес-вызовы, решаемые с помощью ЦДА (сквозные процессы):'

	const linesByKey = {
		'ЦДП|ЦДРБ': `${header}
1. Кросс-функциональное управление ВНС и базовым фондом (ЦДРБ + ЦДП)`,
		'АВНМ|ЦДПР': `${header}
2. Динамическое управление МТО (ЦДПР + АВНМ)
5. Управление подрядными работами и сервисами (ЦДПР + АВНМ и Экосистема)`,
		'АВНМ|ЦДП|ЦДРБ': `${header}
3. Проактивное управление ремонтами и приоритетами (ЦДП + ЦДРБ и АВНМ)`,
		'АВНМ|ЦДПР|ЦДРБ': `${header}
4. Актуализация и оптимизация ресурсной базы (ЦДПР + ЦДРБ + АВНМ)`,
	}

	return linesByKey[key] ?? ''
}
