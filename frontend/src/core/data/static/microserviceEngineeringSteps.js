export function getEngineeringStepsForMicroservice(ms) {
  const steps = ENGINEERING_STEPS_BY_MICROSERVICE[ms]
  if (!steps || steps.length !== 6) {
    throw new Error(`microserviceEngineeringSteps: для «${ms}» ожидается ровно 6 шагов`)
  }
  return [...steps]
}

export function assertEngineeringStepsCoverAllMicroservices() {
  const missing = MICROSERVICES.filter((ms) => !ENGINEERING_STEPS_BY_MICROSERVICE[ms])
  if (missing.length) {
    throw new Error(`microserviceEngineeringSteps: не заданы шаги для:\n${missing.join('\n')}`)
  }
  const extra = Object.keys(ENGINEERING_STEPS_BY_MICROSERVICE).filter((k) => !MICROSERVICES.includes(k))
  if (extra.length) {
    throw new Error(`microserviceEngineeringSteps: лишние ключи:\n${extra.join('\n')}`)
  }
}

