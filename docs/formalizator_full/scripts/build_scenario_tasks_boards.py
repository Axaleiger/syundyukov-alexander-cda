STATUSES = ("завершен", "в работе", "ошибка", "пауза")

DATE_LO = date(2026, 4, 1)
DATE_HI = date(2026, 5, 31)


def strip_stage_number(name: str) -> str:
    """Убирает ведущую нумерацию вида «1. », «12. »."""
    return re.sub(r"^\s*\d+\.\s*", "", name).strip()


def monotonic_due_dates_apr_may_2026(n: int, seed: int) -> list[date]:
    """Случайные даты апрель–май 2026: по порядку карточек даты не убывают; при n ≤ 62 дней в диапазоне — без повтора дня (следующая карточка — не раньше по календарю)."""
    rnd = random.Random(seed)
    span = (DATE_HI - DATE_LO).days
    if n <= span + 1:
        draws = sorted(rnd.sample(range(span + 1), n))
    else:
        draws = sorted(rnd.randint(0, span) for _ in range(n))
    return [DATE_LO + timedelta(days=d) for d in draws]


def random_statuses(n: int, seed: int) -> list[str]:
    rnd = random.Random(seed + 791)
    return [rnd.choice(STATUSES) for _ in range(n)]


def creation_dates_before_due(due_list: list[date], seed: int) -> list[date]:
    """Дата создания не позже срока сдачи, в пределах апреля–мая 2026 где возможно."""
    rnd = random.Random(seed + 13)
    out: list[date] = []
    for due in due_list:
        back = rnd.randint(3, 28)
        c = due - timedelta(days=back)
        if c < DATE_LO:
            c = DATE_LO
        if c > due:
            c = due
        out.append(c)
    return out


def finalize_rows(
    raw: list[tuple[str, str, str, str, str, str]],
    seed: int,
) -> list[list]:
    """raw: этап (с возможной нумерацией), id, название, исполнитель, вход, выход."""
    n = len(raw)
    due = monotonic_due_dates_apr_may_2026(n, seed)
    created = creation_dates_before_due(due, seed)
    statuses = random_statuses(n, seed)
    rows: list[list] = []
    for i, (stage_raw, cid, title, exec_, vin, vout) in enumerate(raw):
        rows.append(
            [
                strip_stage_number(stage_raw),
                cid,
                title,
                exec_,
                COORD,
                due[i],
                statuses[i],
                created[i],
                "",
                vin,
                vout,
            ]
        )
    return rows


